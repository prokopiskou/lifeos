export function clampWeek(week: number) {
  if (week < 1) return 1;
  if (week > 9) return 9;
  return week;
}

export function clampDay(day: number) {
  if (day < 1) return 1;
  if (day > 7) return 7;
  return day;
}

export function getWeekTheme(week: number) {
  return JOURNEY_WEEKS[clampWeek(week)] ?? JOURNEY_WEEKS[1];
}

export function getMicroChallenge(week: number, day: number) {
  const theme = getWeekTheme(week);
  return theme.microChallenges[clampDay(day) - 1] ?? theme.microChallenges[0];
}

